// === is the equality operator
// !== is the no equality operator
// > is the greater than operator 
// < is the less than operator
// >= is the greater than or equals to operator
// <= is the less than or equals to operator

// boolean example
let temp = 135

if (temp <= 32) {
    console.log('It is freezing outside!')
}

if (temp >= 100) {
    console.log('It is way too hot outside!')
}

// Boolean Challange
let age = 65

if (age <= 7) {
    console.log('You will get a child discount!')
}

if (age >= 65) {
    console.log('You will get a senior citizen discount!')
}
